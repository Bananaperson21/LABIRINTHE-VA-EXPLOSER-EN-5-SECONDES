function load_MAZE (wrjgbk){
  MAZE.walls = [];
  MAZE.bombs = [{x:wrjgbk.bomb_coords[0],y:wrjgbk.bomb_coords[1],width:36,height:36}];
  MAZE.portals = [];
  MAZE.maze = wrjgbk;
  
  PLAYER.x = wrjgbk.player_coords[0];
  PLAYER.y = wrjgbk.player_coords[1];
  
  if (wrjgbk===starting_area){current_maze='start';}
  else {current_maze='not start';used_time = 0;}
  
  for(var i = 0; i<wrjgbk.m.length; i+=1){
    for(var j = 0; j<wrjgbk.m[i].length; j+=1){
      if (wrjgbk.m[i][j]===01){
        MAZE.walls.push({x:j*48,y:i*48,width:48,height:48});
      }
      if (wrjgbk.m[i][j]===11){
        MAZE.portals.push({x:j*48,y:i*48,width:48,height:48,m:maze_1,c:'blue'});
      }
      if (wrjgbk.m[i][j]===22){
        MAZE.portals.push({x:j*48,y:i*48,width:48,height:48,m:maze_2,c:'green'});
      }
      if (wrjgbk.m[i][j]===33){
        MAZE.portals.push({x:j*48,y:i*48,width:48,height:48,m:maze_3,c:'yellow'});
      }
      if (wrjgbk.m[i][j]===44){
        MAZE.portals.push({x:j*48,y:i*48,width:48,height:48,m:maze_4,c:'orange'});
      }
      if (wrjgbk.m[i][j]===55){
        MAZE.portals.push({x:j*48,y:i*48,width:48,height:48,m:maze_5,c:'red'});
      }
    }
  }
}





